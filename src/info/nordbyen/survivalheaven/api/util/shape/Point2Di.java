package info.nordbyen.survivalheaven.api.util.shape;

public class Point2Di
{
    private int x;
    private int z;
    
    public Point2Di(final int x, final int z) {
        this.x = x;
        this.z = z;
    }
    
    public int getX() {
        return this.x;
    }
    
    public int getZ() {
        return this.z;
    }
}
